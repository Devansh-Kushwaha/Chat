from OpenGL.GL import *
from OpenGL.GLUT import *
from OpenGL.GLU import *


def draw():
    global text
    global bitmap
    x_offset = 100  
    y_offset = 300  
    pixel_size = 5
    for i in text:
        ma=bitmap[i]
        for j in range(7):
            for k in range(5):
                if ma[j][k]=='1':
                    glPointSize(5)
                    glBegin(GL_POINTS)
                    glVertex2f(x_offset + k * pixel_size, y_offset - j * pixel_size)
                    glEnd()
        x_offset+=6*pixel_size
    
def iterate():
    glViewport(0, 0, 1000, 500)
    glMatrixMode(GL_PROJECTION)
    glLoadIdentity()
    glOrtho(0.0, 1000, 0.0, 500, 0.0, 1.0)
    glMatrixMode (GL_MODELVIEW)
    glLoadIdentity()

def showScreen():
    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT) 
    glLoadIdentity()
    iterate()
    glColor3f(1, 0, 0)
    draw()
    glutSwapBuffers()
    
